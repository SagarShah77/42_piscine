int	ft_sqrt(int nb)
{
	int	sr;

	sr = 1;
	if (nb == 0 || nb == 1)
		return (nb);
	else
	{
		while (sr <= nb)
		{
			if (sr * sr == nb)
				return (sr);
			sr++;
		}
	}
	return (0);
}
