int	ft_is_prime(int nb)
{
	int sagar;

	sagar = 2;
	while (nb % sagar != 0)
	{
		if (sagar > nb)
			break ;
		sagar++;
	}
	if (sagar == nb)
		return (1);
	else
		return (0);
}
