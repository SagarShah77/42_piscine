char	*ft_strcat(char *dest, char *src)
{
	int i;
	int s;

	i = 0;
	s = 0;
	while (dest[s])
		s++;
	while (src[i])
	{
		dest[s] = src[i];
		s++;
		i++;
	}
	dest[s] = '\0';
	return (dest);
}
