char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int s;

	i = 0;
	s = 0;
	while (dest[s])
		s++;
	while (src[i] && (i < nb))
	{
		dest[s] = src[i];
		s++;
		i++;
	}
	dest[s] = '\0';
	return (dest);
}
