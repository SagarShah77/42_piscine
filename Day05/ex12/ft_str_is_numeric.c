int		ncase(char c)
{
	if (c >= 48 && c <= 57)
		return (1);
	return (0);
}

int		ft_str_is_numeric(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (!(ncase(str[i])))
			return (0);
		i++;
	}
	return (1);
}
