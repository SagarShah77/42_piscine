int		lcase(char c)
{
	if (c >= 97 && c <= 122)
		return (1);
	return (0);
}

int		ft_str_is_lowercase(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (!(lcase(str[i])))
			return (0);
		i++;
	}
	return (1);
}
